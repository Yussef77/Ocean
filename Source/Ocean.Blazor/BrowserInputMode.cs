namespace Oceanware.Ocean.Blazor {

    /// <summary>
    /// Represents the values for the enum BrowserInputMode.
    /// </summary>
    public enum BrowserInputMode {

        /// <summary>
        /// None, inputmode will not be added to the input control.
        /// </summary>
        None,

        /// <summary>
        /// Decimal, inputmode decimal will be added to the input control.
        /// </summary>
        Decimal,

        /// <summary>
        /// Numeric, inputmode numeric will be added to the input control.
        /// </summary>
        Numeric
    }
}
